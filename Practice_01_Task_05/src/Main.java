		System.out.println("Введите значение стороны b (cm): ");
		int signB = scanner.nextInt();
		
		System.out.println("Введите значение стороны c (cm): ");
		int signC = scanner.nextInt();
		
		int volume = signA * signB * signC;
		
		System.out.println("Объем параллелепипеда " + "(" + signA + " x " 
				+ signB + " x " + signC + ")" + " Равен " + volume + 
				" cm2");
		
